package withi_ide_package;

public class Q8 {
    public static void main(String[] args) {
        int [][]arr = {
                {1,2,3,4,5},
                {5,4,3,2,1},
                {7,8,9,1,5,7,7},
        };
        for (int i = 0; i < arr.length; i++) {
            System.out.println();
            for (int j = 0; j < arr[i].length; j++) {
                System.out.print(arr[i][j] +"  ");
            }
        }
    }
}
